export const METRIC_ICON_OPTIONS = Object.keys(ICON_MAP)

export function normalizeMetricIconName(value = '') {
  const iconName = String(value || '').trim()
  return ICON_MAP[iconName] ? iconName : 'Activity'
}

export function MetricIcon({ name = 'Activity', size = 16, ...props }) {
  const safeName = normalizeMetricIconName(name)
  const Icon = ICON_MAP[safeName] || Activity

  return <Icon size={size} {...props} />
}
