import { generateKeyPairSync } from 'node:crypto'
import { chmod, mkdir, readFile, writeFile } from 'node:fs/promises'
import os from 'node:os'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

import { publicKeyFingerprint } from '../lib/release-signing.mjs'

const scriptsDir = path.dirname(fileURLToPath(import.meta.url))
const serviceDir = path.resolve(scriptsDir, '..')
const repoRoot = path.resolve(serviceDir, '..', '..')

function parseArgs(argv) {
  const values = {}
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index]
    if (!token.startsWith('--')) continue
    const key = token.slice(2)
    const next = argv[index + 1]
    if (!next || next.startsWith('--')) values[key] = true
    else {
      values[key] = next
      index += 1
    }
  }
  return values
}

function escapeCppString(value) {
  return String(value).replace(/\\/g, '\\\\').replace(/"/g, '\\"')
}

const args = parseArgs(process.argv.slice(2))
const keyId = String(args['key-id'] || process.env.OTA_RELEASE_KEY_ID || 'dotwatch-release-2026-01').trim()
if (!/^[A-Za-z0-9][A-Za-z0-9_.-]{0,95}$/.test(keyId)) {
  throw new Error('--key-id contains invalid characters')
}

const defaultPrivatePath = path.join(os.homedir(), '.dotwatch', 'ota-signing', `${keyId}.private.pem`)
const privateKeyPath = path.resolve(String(args['private-key'] || defaultPrivatePath))
const publicKeyPath = path.resolve(
  String(args['public-key'] || path.join(serviceDir, 'keys', 'release-signing.public.pem'))
)
const headerPath = path.resolve(
  String(args.header || path.join(repoRoot, 'esp32', 'dotwatch_esp32_product', 'include', 'OtaSigningKey.h'))
)
const metadataPath = path.resolve(
  String(args.metadata || path.join(serviceDir, 'keys', 'release-signing-key.json'))
)
const force = args.force === true || String(args.force || '').toLowerCase() === 'true'

if (privateKeyPath.startsWith(`${repoRoot}${path.sep}`)) {
  throw new Error('Private signing key must be stored outside the dotwatch repository')
}

if (!force) {
  try {
    await readFile(privateKeyPath)
    throw new Error(`Private key already exists: ${privateKeyPath}. Use --force only for an intentional rotation.`)
  } catch (error) {
    if (error?.code !== 'ENOENT') throw error
  }
}

const { privateKey, publicKey } = generateKeyPairSync('ec', {
  namedCurve: 'prime256v1',
  privateKeyEncoding: { type: 'pkcs8', format: 'pem' },
  publicKeyEncoding: { type: 'spki', format: 'pem' },
})
const fingerprint = publicKeyFingerprint(publicKey)

await mkdir(path.dirname(privateKeyPath), { recursive: true })
await mkdir(path.dirname(publicKeyPath), { recursive: true })
await mkdir(path.dirname(headerPath), { recursive: true })
await mkdir(path.dirname(metadataPath), { recursive: true })

await writeFile(privateKeyPath, privateKey, { encoding: 'utf8', mode: 0o600 })
try {
  await chmod(privateKeyPath, 0o600)
} catch {
  // Windows ACLs are managed by the user/OS; chmod is best-effort.
}
await writeFile(publicKeyPath, publicKey, 'utf8')

const header = `#pragma once\n\n// Generated by services/ota-server/scripts/generate-signing-key.mjs.\n// The private key is intentionally stored outside the repository.\n#define DOTWATCH_OTA_SIGNING_KEY_CONFIGURED 1\n#define DOTWATCH_OTA_SIGNING_KEY_ID "${escapeCppString(keyId)}"\n#define DOTWATCH_OTA_SIGNING_PUBLIC_KEY_SHA256 "${fingerprint}"\n\nstatic constexpr const char *DOTWATCH_OTA_SIGNING_PUBLIC_KEY_PEM = R"DOTWATCH_OTA_KEY(${publicKey.trim()}\n)DOTWATCH_OTA_KEY";\n`
await writeFile(headerPath, header, 'utf8')

const metadata = {
  schemaVersion: 1,
  keyId,
  algorithm: 'ecdsa-p256-sha256',
  publicKeySha256: fingerprint,
  publicKeyFile: path.relative(repoRoot, publicKeyPath).replaceAll(path.sep, '/'),
  firmwareHeader: path.relative(repoRoot, headerPath).replaceAll(path.sep, '/'),
  generatedAt: new Date().toISOString(),
}
await writeFile(metadataPath, `${JSON.stringify(metadata, null, 2)}\n`, 'utf8')

console.log('OTA release signing key generated')
console.log(`Key ID      : ${keyId}`)
console.log(`Fingerprint : ${fingerprint}`)
console.log(`Private key : ${privateKeyPath}`)
console.log(`Public key  : ${publicKeyPath}`)
console.log(`ESP32 header: ${headerPath}`)
console.log('IMPORTANT: Back up the private key securely. Never commit or upload it to Render.')
