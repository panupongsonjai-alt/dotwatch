import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const previewDir = path.dirname(fileURLToPath(import.meta.url));
const portalDir = path.resolve(previewDir, "../src/portal");
const outputPath = path.join(portalDir, "PortalAssets.h");

function normalizeAsset(text, leadingComment) {
  return text
    .replace(leadingComment, "")
    .replace(/\r\n/g, "\n")
    .trim();
}

const css = normalizeAsset(
  await readFile(path.join(previewDir, "src/portal.css"), "utf8"),
  "/* Shared with ESP32 firmware. Run `npm run sync` after editing. */"
);

const firmwareJs = normalizeAsset(
  await readFile(path.join(previewDir, "src/firmware.js"), "utf8"),
  "// Shared with ESP32 firmware. Run `npm run sync` after editing."
);

for (const [label, content, delimiter] of [
  ["CSS", css, "DOTWATCH_CSS"],
  ["JavaScript", firmwareJs, "DOTWATCH_JS"]
]) {
  if (content.includes(`)${delimiter}\"`)) {
    throw new Error(`${label} contains the reserved raw-string delimiter: )${delimiter}\"`);
  }
}

const header = `#pragma once

#include <Arduino.h>

// Generated from portal-preview/src/portal.css and portal-preview/src/firmware.js.
// Do not edit this file directly. Run: npm run sync

static const char DOTWATCH_PORTAL_CSS[] PROGMEM = R"DOTWATCH_CSS(
${css}
)DOTWATCH_CSS";

static const char DOTWATCH_PORTAL_JS[] PROGMEM = R"DOTWATCH_JS(
${firmwareJs}
)DOTWATCH_JS";
`;

await writeFile(outputPath, header, "utf8");
console.log(`Updated: ${outputPath}`);
console.log(`CSS bytes: ${Buffer.byteLength(css, "utf8")}`);
console.log(`JS bytes : ${Buffer.byteLength(firmwareJs, "utf8")}`);
